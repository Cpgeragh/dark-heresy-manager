// ─── Types ───────────────────────────────────────────────────────────────────

export interface GrenadeRef {
  id: string;
  name: string;
  source: string;
  class: string;
  /** Damage string, "—" if no damage, or "Special" */
  damage: string;
  /** Penetration, "—" if not applicable */
  pen: string;
  /** Special rules string, "—" if none */
  specialRules: string;
  weight: string;
  value: string;
  rarity: string;
  /** Full rules text for info modal */
  description?: string;
}

export interface RangedWeaponRef {
  id: string;
  name: string;
  source: SkillSource;
  class: string;
  range: string;
  rof: string;
  damage: string;
  pen: number;
  clip: number;
  reload: string;
  specialRules: string;
  weight: string;
  value: string;
  rarity: string;
}

